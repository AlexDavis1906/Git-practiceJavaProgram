package Homework;
import java.util.*;

public class StringsPractice {
    public static void main(String[] args) {
        Scanner sc=new Scanner(System.in);
        String str=sc.nextLine();
        int i;
        for (i=str.length() -1; i>=0; i--){
            System.out.println(str.charAt(i));

        }



    }
}
